#include<stdio.h>
int main()
{
	int a;
	a=10;
	printf("integer value=%d\n",a);
	float b;
	b=2.5;
	printf("float value=%f\n",b);
	
	char c;
	c='E';
	printf("character value=%c\n",c);
	long int d;
	d=45678;
	printf("long integer value=%ld\n",d);
	double e;
	e=298.906785;
	printf("long float value=%d\n",e);

}
